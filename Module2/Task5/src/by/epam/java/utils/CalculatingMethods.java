package by.epam.java.utils;



public class CalculatingMethods {
	
		
		
 public static int Summing(int[] args) {
	 
	 int sum = 0;
	 
	 for (int i = 0 ; i < args.length; i++ ) {
		 
		sum += args[i]; 
		 
	 }
	 
	 return sum;
 }

 public static double Summing (double[] args) {
	 
	 double sum = 0;
	 
	 for (int i = 0 ; i < args.length; i++ ) {
		 
		sum += args[i]; 
		 
	 }
	 
	 return sum;
 }
 
 

}
